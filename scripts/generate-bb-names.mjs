#!/usr/bin/env node
/**
 * Generates official-bb-names.generated.ts for @aws-blocks/core.
 *
 * Reads the vendorize map from packages/blocks/package.json and extracts all
 * Building Block names into a deduplicated Set exported as OFFICIAL_BB_NAMES.
 *
 * Usage:
 *   node scripts/generate-bb-names.mjs
 *
 * Designed to be called from packages/core's `prebuild` npm script.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '..');

const blocksPkgPath = join(repoRoot, 'packages', 'blocks', 'package.json');
const blocksPkg = JSON.parse(readFileSync(blocksPkgPath, 'utf-8'));

const vendorize = blocksPkg['aws-blocks']?.vendorize;
if (!vendorize || typeof vendorize !== 'object') {
	console.error('ERROR: Could not find aws-blocks.vendorize in packages/blocks/package.json');
	process.exit(1);
}

// The vendorize map uses class export names (e.g. "AuthOIDC"), but telemetry
// needs runtime `bbName` values (e.g. "AuthOidc"), which occasionally differ.
// This mapping bridges the gap so both vendorize-map and telemetry tests pass.
const BB_NAME_OVERRIDES = {
	'AuthOIDC': 'AuthOidc',
};

// Flatten all arrays, apply overrides, and deduplicate
const allNames = [...new Set(
	Object.values(vendorize).flat().map(name => BB_NAME_OVERRIDES[name] ?? name)
)].sort();

const outputPath = join(repoRoot, 'packages', 'core', 'src', 'common', 'official-bb-names.generated.ts');

const content = `// DO NOT EDIT — auto-generated by scripts/generate-bb-names.mjs
// Source: packages/blocks/package.json → aws-blocks.vendorize

/**
 * Set of official Building Block display names recognized by telemetry.
 * Any BB whose \`bbName\` matches one of these entries is reported in the
 * \`product.buildingBlocks\` array; others still contribute to \`totalCount\`.
 */
export const OFFICIAL_BB_NAMES: ReadonlySet<string> = new Set([
${allNames.map(name => `  '${name}',`).join('\n')}
]);
`;

writeFileSync(outputPath, content);
console.log(`Generated official-bb-names.generated.ts (${allNames.length} BB names)`);
