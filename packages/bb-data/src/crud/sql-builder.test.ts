test('buildUpdate: auto-generated columns excluded', () => {
  const q = buildUpdate('todos', ['todo-1'], { content: 'x', updated_at: 'now' }, todosMeta);
  assert.ok(!q.text.includes('"updated_at"'));
  assert.deepStrictEqual(q.params, ['x', 'todo-1']);
});

test('buildUpdate: empty data throws', () => {
  assert.throws(
    () => buildUpdate('todos', ['todo-1'], {}, todosMeta),
    /No columns to update/,
  );
});

