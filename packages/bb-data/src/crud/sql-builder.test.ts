  const q = buildUpdate('todos', ['todo-1'], { content: 'Updated', done: true }, todosMeta);
  assert.strictEqual(q.text, 'UPDATE "todos" SET "content" = $1, "done" = $2 WHERE "id" = $3 RETURNING *');
