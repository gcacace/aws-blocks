test('buildSelect: full query with all options', () => {
  const q = buildSelect('todos', {
    select: ['id', 'content', 'done'],
    where: { done: false, priority: { gt: 3 } } as any,
    orderBy: ['created_at:desc'],
    limit: 20,
    offset: 0,
  }, todosMeta);
  assert.strictEqual(
    q.text,
    'SELECT "id", "content", "done" FROM "todos" WHERE "done" = $1 AND "priority" > $2 ORDER BY "created_at" DESC LIMIT $3 OFFSET $4',
  );
  assert.deepStrictEqual(q.params, [false, 3, 20, 0]);
});

