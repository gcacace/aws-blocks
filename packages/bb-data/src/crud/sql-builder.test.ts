// ─── buildWhere — or-clause operator validation ─────────────────────────────
// The `or` branch must validate operator inputs the same way the top-level
// branch does. Empty/non-array `in` values must be rejected, not produce
// invalid SQL or uncaught TypeErrors.

test('buildWhere: empty "in" array at top level throws (control)', () => {
  assert.throws(
    () => buildWhere({ priority: { in: [] } } as any, todosMeta),
    /"in" requires a non-empty array/,
  );
});

test('buildWhere: empty "in" array inside "or" throws (not invalid SQL)', () => {
  assert.throws(
    () => buildWhere({ or: [{ priority: { in: [] } }] } as any, todosMeta),
    /"in" requires a non-empty array/,
  );
});

test('buildWhere: non-array "in" value inside "or" throws (not an uncaught TypeError)', () => {
  assert.throws(
    () => buildWhere({ or: [{ priority: { in: 'not-an-array' } }] } as any, todosMeta),
    /"in" requires a non-empty array/,
  );
});

test('buildWhere: valid "in" inside "or" still builds correctly', () => {
  const { clause, params } = buildWhere(
    { or: [{ priority: { in: [1, 2] } }, { done: true }] } as any,
    todosMeta,
  );
  assert.ok(clause.includes('IN ($1, $2)'), `expected IN with two params, got: ${clause}`);
  assert.deepStrictEqual(params, [1, 2, true]);
});

test('buildWhere: unknown operator inside "or" throws (parity with top level)', () => {
  assert.throws(
    () => buildWhere({ or: [{ priority: { bogus: 1 } }] } as any, todosMeta),
    /Unknown operator: "bogus"/,
  );
});

// ─── buildSelect — orderBy direction validation ─────────────────────────────
// orderBy must be in "column:asc" or "column:desc" format. Invalid directions,
// missing directions, and extra colon segments must be rejected.

test('buildSelect: invalid orderBy direction throws instead of defaulting to ASC', () => {
  assert.throws(
    () => buildSelect('todos', { orderBy: 'priority:invalid' }, todosMeta),
    /direction/i,
  );
});

test('buildSelect: orderBy with extra colon segment throws', () => {
  assert.throws(
    () => buildSelect('todos', { orderBy: 'priority:desc:extra' }, todosMeta),
    /direction|orderBy/i,
  );
});

test('buildSelect: orderBy without a direction throws (must be explicit asc/desc)', () => {
  assert.throws(
    () => buildSelect('todos', { orderBy: 'priority' }, todosMeta),
    /direction|orderBy/i,
  );
});

test('buildSelect: valid asc/desc directions still work', () => {
  assert.strictEqual(
    buildSelect('todos', { orderBy: 'priority:asc' }, todosMeta).text,
    'SELECT * FROM "todos" ORDER BY "priority" ASC',
  );
  assert.strictEqual(
    buildSelect('todos', { orderBy: 'priority:DESC' }, todosMeta).text,
    'SELECT * FROM "todos" ORDER BY "priority" DESC',
  );
});
