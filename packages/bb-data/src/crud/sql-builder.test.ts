// ── buildInsert ────────────────────────────────────────────────────────

test('buildInsert: basic insert excludes auto-generated', () => {
  const q = buildInsert('todos', { content: 'Buy milk', done: false, user_id: 'u1' }, todosMeta);
  assert.strictEqual(q.text, 'INSERT INTO "todos" ("content", "done", "user_id") VALUES ($1, $2, $3) RETURNING *');
  assert.deepStrictEqual(q.params, ['Buy milk', false, 'u1']);
});

test('buildInsert: auto-generated columns are excluded', () => {
  const q = buildInsert('todos', { id: 'manual', content: 'test', done: true, user_id: 'u1' }, todosMeta);
  // id is auto-generated, should be excluded
  assert.ok(!q.text.includes('"id"'));
  assert.deepStrictEqual(q.params, ['test', true, 'u1']);
});

test('buildInsert: all auto-generated throws', () => {
  assert.throws(
    () => buildInsert('todos', { id: 'x', created_at: 'now', updated_at: 'now' }, todosMeta),
    /No columns to insert/,
  );
});

test('buildInsert: unknown column throws', () => {
  assert.throws(
    () => buildInsert('todos', { bad_col: 'x' }, todosMeta),
    /Unknown column: "bad_col"/,
  );
});

